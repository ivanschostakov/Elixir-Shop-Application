function humanizeTranslationKey(key: TranslationKey) {
    const segments = key.split(".")
    const lastSegment = segments[segments.length - 1] ?? key
    return lastSegment
        .replace(/([a-z])([A-Z])/g, "$1 $2")
        .replace(/[_-]+/g, " ")
        .replace(/\b\w/g, (match) => match.toUpperCase())
}

export function translate(key: TranslationKey, language: Language = "ru") {
    if (language === "en") {
        return enTranslations[key] ?? humanizeTranslationKey(key)
    }

    return ruTranslations[key]
}
